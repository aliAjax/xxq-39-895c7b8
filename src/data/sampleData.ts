const createDefaultTasks = (completedCount: number = 0): ProductionTask[] => {
  const now = Date.now();
  return DEFAULT_TASK_TYPES.map((type, index) => ({
    id: `sample-task-${now}-${index}`,
    type,
    name: TASK_TYPE_LABELS[type],
    completed: index < completedCount,
    createdAt: now,
    updatedAt: now,
  }));
};

